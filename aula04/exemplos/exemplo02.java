package exemplos;

public class exemplo02 {
    public static void main(String[] args) {

        // printf(FORMATO, DADOS)
        // FORMATO:
        // %d = iinteiro
        // %f + decimal
        // %s = texto
        // \n = pul de linha

        // %.2f = 2 casas decimais, comm arredondamento

        System.out.printf("O número é %d\n", 10);

        System.out.printf("O número são %d e %d\n", 10, 15);

        System.out.printf("A média é %f\n", 45.6754321);

        System.out.printf("A média é %.2f\n", 45.6754321);

    }
}
